    void setData(){
        DefaultTableModel dtm = (DefaultTableModel)dataTable.getModel();
        dtm.setNumRows(0);
        for(Person p : pd.getPersons()){
            Object[] row = new Object[4];
            row[0] = p.getName();
            row[1] = p.getCity();
            row[2] = p.getState();
            row[3] = p.getCountry();
            dtm.addRow(row);
        }  
    }
